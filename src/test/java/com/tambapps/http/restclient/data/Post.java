public class Post {

  private Integer id;
  private Integer userId;
  private String title;
  private String body;

  public Integer getId() {
    return id;
  }

  public Integer getUserId() {
    return userId;
  }

  public String getTitle() {
    return title;
  }

  public String getBody() {
    return body;
  }
}
